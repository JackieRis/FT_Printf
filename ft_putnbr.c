#include "ft_printf.h"

void    ft_putnbr(int n, int *i, int fd)
{
    if (n == -2147483648)
        (*i) = write (fd, "-2147483648", 11);
    if (n < 0)
    {
        (*i) = write (fd, "-", 1);
        n *= -1;
    }
    if (n > 9)
        ft_putnbr(n / 10, i, fd);
    (*i) += ft_putchar_fd(n % 10 + '0', fd);
}
